/*********************************************************************************************************************
* 文件名称          control
* 功能描述          智能车摄像头扫描巡线 - 舵机与电机控制头文件
* 适用平台          MM32F327X_G8P
* 说明              提供舵机角度控制和双侧电机差速控制接口
*
* 舵机控制：
*   servo_set_angle(float angle) —— 控制舵机转到指定角度（0~180°）
*   限幅宏 SERVO_ANGLE_MIN / SERVO_ANGLE_MAX 定义安全范围
*
* 电机控制：
*   motor_set_duty(int16 left_duty, int16 right_duty) —— 控制双侧电机
*   占空比范围 -100~100（负值反转），限幅宏 MOTOR_DUTY_MAX 限制绝对值
*********************************************************************************************************************/

#ifndef __CONTROL_H_
#define __CONTROL_H_

#include "zf_common_headfile.h"

//==================================================== 舵机引脚与参数配置 ====================================================

#define SERVO_PWM                   (TIM2_PWM_CH1_A15)                          // 舵机 PWM 引脚（A15）
#define SERVO_FREQ                  (50)                                        // 舵机频率 50Hz（周期 20ms）
                                                                                // 范围 50~300Hz，常用 50Hz

#define SERVO_ANGLE_MIN             (-12.0f)                                     // 舵机左边界角度（防止打角过大损坏机械结构）
#define SERVO_ANGLE_MAX             (12.0f)                                    // 舵机右边界角度

// 舵机占空比计算公式：
// 0.5ms~2.5ms 高电平对应 0°~180°
// 占空比 = PWM_DUTY_MAX / (1000/freq) * (0.5 + angle/180*2)
// 即     = PWM_DUTY_MAX / (1000/freq) * (0.5 + angle/90)
#define SERVO_DUTY(angle)           ((uint32)((float)PWM_DUTY_MAX / (1000.0f / (float)SERVO_FREQ) * (0.5f + (float)(angle) / 90.0f)))

//==================================================== 电机引脚与参数配置 ====================================================

#define MOTOR_L_DIR                 (A0)                                        // 左电机方向引脚
#define MOTOR_L_PWM                 (TIM5_PWM_CH2_A1)                           // 左电机 PWM 引脚

#define MOTOR_R_DIR                 (A2)                                        // 右电机方向引脚
#define MOTOR_R_PWM                 (TIM5_PWM_CH4_A3)                           // 右电机 PWM 引脚

#define MOTOR_PWM_FREQ              (17000)                                     // 电机 PWM 频率 17KHz

#define MOTOR_DUTY_MAX              (100)                                       // 电机占空比最大绝对值（0~100）

//==================================================== 控制函数声明 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     控制模块初始化（舵机 PWM + 双电机 GPIO/PWM）
// 参数说明     void
// 返回参数     void
// 使用示例     control_init();
// 备注信息     在 main() 初始化阶段调用，必须在时钟和外设初始化后
//-------------------------------------------------------------------------------------------------------------------
void control_init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     设置舵机角度
// 参数说明     angle —— 目标角度（-64.0 ~ 116.0）
// 限幅         -12.0° ≤ angle ≤ 12.0°（防止打死方向损坏机械结构）
// 返回参数     void
// 使用示例     servo_set_angle(0.0f);  // 舵机转到正中
// 备注信息     小于0，舵机向左转；大于0，舵机向右转；0°为正中
//-------------------------------------------------------------------------------------------------------------------
void servo_set_angle(float angle);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     设置双侧电机转速（支持差速）
// 参数说明     left_duty  —— 左电机占空比（-100 ~ 100，正值前进/负值后退）
// 参数说明     right_duty —— 右电机占空比（-100 ~ 100，正值前进/负值后退）
// 返回参数     void
// 使用示例     motor_set_duty(50, 30);  // 左轮快、右轮慢，向右转
//              motor_set_duty(0, 0);    // 刹车/停止
// 备注信息     占空比自动限幅到 [-MOTOR_DUTY_MAX, MOTOR_DUTY_MAX]
//              正值=DIR高电平（前进），负值=DIR低电平（后退），0=停止
//-------------------------------------------------------------------------------------------------------------------
void motor_set_duty(int16 left_duty, int16 right_duty);

#endif
