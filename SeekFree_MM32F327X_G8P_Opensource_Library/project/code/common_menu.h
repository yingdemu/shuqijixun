/*********************************************************************************************************************
* 文件名称          common_menu
* 功能描述          智能车摄像头扫描巡线 - 多级菜单数据结构（纯数据层，无UI依赖）
* 适用平台          MM32F327X_G8P
* 说明              移植自 WwuSama 多级菜单 v4.0.0
*                   本文件仅定义菜单的树形数据结构和创建/导航函数
*                   不包含任何显示或输入相关的代码
*                   菜单结构使用双向循环链表实现多级嵌套
*
* 数据结构：
*   Folder_Menu 是菜单树的基本节点：
*     - father/ son_first          → 父子关系（纵向层级）
*     - next_brother/ last_brother → 兄弟关系（横向同级）
*     - private_data               → 指向实际变量的指针（参数绑定）
*     - kind                       → 节点类型（文件夹/布尔/数值等）
*********************************************************************************************************************/

#ifndef __COMMON_MENU_H_
#define __COMMON_MENU_H_

#include "zf_common_headfile.h"

//==================================================== 菜单节点类型枚举 ====================================================

// 菜单项的数据类型 —— 决定了 private_data 指向的变量类型
typedef enum
{
    Normal_Folder,                                                              // 文件夹（无数据，仅用于组织子菜单）
    bool_Box,                                                                   // 布尔型（true/false 切换）
    int_Box,                                                                    // int 型
    float_Box,                                                                  // float 型
    uint8_Box,                                                                  // uint8 型
    int8_Box,                                                                   // int8 型
    uint16_Box,                                                                 // uint16 型
    int16_Box,                                                                  // int16 型
    uint32_Box,                                                                 // uint32 型
    int32_Box,                                                                  // int32 型
}Folder_Class;

//==================================================== 菜单节点结构体 ====================================================

// 菜单树的一个节点 —— 可以是文件夹（包含子节点）或数据项（绑定变量）
typedef struct Folder_Menu
{
    const char *name;                                                           // 节点名称（显示用字符串）
    uint8 No;                                                                   // 当前节点在父节点下的位次编号（从1开始）
    Folder_Class kind;                                                          // 节点类型（文件夹 or 数据类型）

    uint8 sons_Count;                                                           // 子节点个数（仅文件夹有效）

    struct Folder_Menu *father;                                                 // 指向父节点的指针（根节点的 father 为 NULL）
    struct Folder_Menu *son_first;                                              // 指向第一个子节点的指针（仅文件夹有效）
    struct Folder_Menu *next_brother;                                           // 指向下一个兄弟节点的指针
    struct Folder_Menu *last_brother;                                           // 指向上一个兄弟节点的指针

    void *private_data;                                                         // 指向绑定变量的指针（数据项有效，文件夹为 NULL）
    uint8 number_box_select;                                                    // 数值项编辑选中标志（1=当前正在编辑此项）

    // ---- 数值限幅（3.3版本新增） ----
    uint8 isLimit;                                                              // 是否启用限幅（1=启用）
    float limit_min;                                                            // 最小值限制
    float limit_max;                                                            // 最大值限制

}Folder_Menu;

//==================================================== 内存池配置 ====================================================

#define MAX_MENU_ITEMS          (64)                                            // 菜单内存池大小（最多可动态创建的节点数）
                                                                                // 可根据需要增大，但会占用更多RAM

//==================================================== 外部变量声明 ====================================================

extern Folder_Menu myMenu;                                                      // 菜单根节点（主菜单）
extern Folder_Menu *key_menu_p;                                                 // 当前按键操作指向的菜单节点（光标所在项）

//==================================================== 菜单创建函数 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     向父节点注册一个子文件夹
// 参数说明     father —— 父节点指针
// 参数说明     me     —— 要注册的文件夹节点指针
// 参数说明     name   —— 文件夹名称字符串
// 返回参数     void
// 使用示例     create_Menu_Folder(&myMenu, &folder1, "servo_pid");
//-------------------------------------------------------------------------------------------------------------------
void create_Menu_Folder(Folder_Menu *father, Folder_Menu *me, const char *name);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     向父节点注册一个布尔型复选框
// 参数说明     father —— 父节点指针
// 参数说明     me     —— 要注册的复选框节点指针
// 参数说明     name   —— 复选框名称字符串
// 参数说明     check  —— 指向 bool 变量的指针（用于读写状态）
// 返回参数     void
// 使用示例     create_Menu_BoolBox(&myMenu, &box1, "enable", &my_bool_var);
//-------------------------------------------------------------------------------------------------------------------
void create_Menu_BoolBox(Folder_Menu *father, Folder_Menu *me, const char *name, void *check);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     向父节点注册一个数值型参数（无限制）
// 参数说明     father —— 父节点指针
// 参数说明     me     —— 要注册的数值框节点指针
// 参数说明     name   —— 参数名称字符串
// 参数说明     number —— 指向数值变量的指针
// 参数说明     kind   —— 数值类型（int_Box, float_Box, uint8_Box 等）
// 返回参数     void
// 使用示例     create_Menu_NumberBox(&folder1, &numbox, "servo_kp", &servo_kp, float_Box);
//-------------------------------------------------------------------------------------------------------------------
void create_Menu_NumberBox(Folder_Menu *father, Folder_Menu *me, const char *name, void *number, Folder_Class kind);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     向父节点注册一个带限幅的数值型参数
// 参数说明     father    —— 父节点指针
// 参数说明     me        —— 要注册的数值框节点指针
// 参数说明     name      —— 参数名称字符串
// 参数说明     number    —— 指向数值变量的指针
// 参数说明     kind      —— 数值类型
// 参数说明     limit_Min —— 最小值限制
// 参数说明     limit_Max —— 最大值限制
// 返回参数     void
// 使用示例     create_Menu_LimitNumberBox(&f1, &nb, "kp", &kp, float_Box, 0.0f, 100.0f);
//-------------------------------------------------------------------------------------------------------------------
void create_Menu_LimitNumberBox(Folder_Menu *father, Folder_Menu *me, const char *name, void *number, Folder_Class kind, float limit_Min, float limit_Max);

//==================================================== 动态菜单创建函数 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     动态创建一个子文件夹（从内存池中分配节点）
// 参数说明     father —— 父节点指针
// 参数说明     name   —— 文件夹名称字符串
// 返回参数     Folder_Menu* —— 新创建的文件夹节点指针（NULL=内存池已满）
// 使用示例     Folder_Menu *folder = dynamicCreate_Menu_Folder(&myMenu, "servo_pid");
// 备注信息     推荐使用动态创建方式，无需手动管理节点内存
//-------------------------------------------------------------------------------------------------------------------
Folder_Menu *dynamicCreate_Menu_Folder(Folder_Menu *father, const char *name);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     动态创建一个数值型参数（从内存池中分配节点）
// 参数说明     father —— 父节点指针
// 参数说明     name   —— 参数名称字符串
// 参数说明     number —— 指向数值变量的指针
// 参数说明     kind   —— 数值类型
// 返回参数     Folder_Menu* —— 新创建的节点指针
// 使用示例     dynamicCreate_Menu_NumberBox(&folder, "servo_kp", &servo_kp, float_Box);
//-------------------------------------------------------------------------------------------------------------------
Folder_Menu *dynamicCreate_Menu_NumberBox(Folder_Menu *father, const char *name, void *number, Folder_Class kind);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     动态创建一个带限幅的数值型参数（从内存池中分配节点）
// 参数说明     father    —— 父节点指针
// 参数说明     name      —— 参数名称字符串
// 参数说明     number    —— 指向数值变量的指针
// 参数说明     kind      —— 数值类型
// 参数说明     limit_Min —— 最小值限制
// 参数说明     limit_Max —— 最大值限制
// 返回参数     Folder_Menu* —— 新创建的节点指针
//-------------------------------------------------------------------------------------------------------------------
Folder_Menu *dynamicCreate_Menu_LimitNumberBox(Folder_Menu *father, const char *name, void *number, Folder_Class kind, float limit_Min, float limit_Max);

//==================================================== 菜单初始化函数 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     菜单环形链表初始化（将兄弟链表的尾部与头部相连，形成循环链表）
// 参数说明     Menu —— 菜单根节点指针
// 返回参数     void
// 使用示例     All_Folder_Menu_Init(&myMenu);
// 备注信息     在创建完所有菜单节点后调用一次
//              递归遍历所有子节点，将每层兄弟链表的尾节点与首节点相连
//-------------------------------------------------------------------------------------------------------------------
void All_Folder_Menu_Init(Folder_Menu *Menu);

#endif
