/*********************************************************************************************************************
* 文件名称          common_Mymenu
* 功能描述          智能车摄像头扫描巡线 - 菜单内容定义与按键交互头文件
* 适用平台          MM32F327X_G8P
* 说明              本文件定义：
*                     1. 菜单的具体内容（主菜单 + servo_pid/motor_pid 子菜单）
*                     2. 按键交互函数声明（四键操作菜单）
*                     3. IPS200 屏幕显示菜单文字
*
* 按键操作（使用主板上的 KEY_1 ~ KEY_4）：
*   KEY_1 → 光标上移 / 参数增大（编辑模式下短按=步进增加，长按=10倍快速增加）
*   KEY_2 → 光标下移 / 参数减小（编辑模式下短按=步进减小，长按=10倍快速减小）
*   KEY_3 → 确认 / 进入子菜单 / 选中编辑参数 / 保存并退出编辑
*   KEY_4 → 返回上级 / 取消编辑 / 切换步进值 / 退出图像模式
*
* 主菜单结构：
*   <Menu>
*     ├── image          → 进入后 IPS200 显示摄像头灰度图像（KEY_4退出）
*     ├── servo_pid      → 舵机PID参数子菜单
*     │   ├── servo_kp   (float, 0.00~100.00, 步进可调)
*     │   ├── servo_ki   (float, 0.00~10.00,  步进可调)
*     │   └── servo_kd   (float, 0.00~100.00, 步进可调)
*     └── motor_pid      → 电机PID参数子菜单
*         ├── motor_kp   (float, 0.00~100.00, 步进可调)
*         ├── motor_ki   (float, 0.00~10.00,  步进可调)
*         └── motor_kd   (float, 0.00~100.00, 步进可调)
*********************************************************************************************************************/

#ifndef __COMMON_MYMENU_H_
#define __COMMON_MYMENU_H_

#include "common_menu.h"
#include "zf_device_ips200.h"
#include "zf_device_key.h"

//==================================================== 显示格式配置 ====================================================

// IPS200 屏幕分辨率 320×240，使用 8×16 字体
// 320/8=40字/行，240/16=15行可用（第0行标题，第1-13行菜单项，第14行底部提示）
#define FONT_W                  (8)                                             // 字体宽度（像素）
#define FONT_H                  (16)                                            // 字体高度（像素）
#define MENU_TITLE_Y            (0)                                             // 标题栏 Y 坐标（第0行）
#define MENU_START_Y            (16)                                            // 菜单项起始 Y 坐标（第1行，跳过标题）
#define MENU_FOOTER_LINE        (14)                                            // 底部提示栏所在行号（240/16=15行，最后一行=14）
#define MENU_FOOTER_Y           (MENU_FOOTER_LINE * FONT_H)                     // 底部提示栏 Y 坐标 = 224
#define MENU_MAX_DISPLAY_ITEMS  (MENU_FOOTER_LINE - 1)                          // 最大可显示的菜单项数（13行）
#define FOLDER_NAME_LEN         (14)                                            // 菜单项名称最大显示长度
#define FOLDER_NUMBER_LEN       (8)                                             // 参数值最大显示长度

//==================================================== 步进值配置 ====================================================

#define SETUP_LEN               (7)                                             // 步进值数组长度

//==================================================== PID参数变量声明 ====================================================

extern float servo_kp;                                                          // 舵机比例系数 Kp（默认 1.0）
extern float servo_ki;                                                          // 舵机积分系数 Ki（默认 0.0）
extern float servo_kd;                                                          // 舵机微分系数 Kd（默认 0.5）

extern float motor_kp;                                                          // 电机比例系数 Kp（默认 1.0）
extern float motor_ki;                                                          // 电机积分系数 Ki（默认 0.1）
extern float motor_kd;                                                          // 电机微分系数 Kd（默认 0.0）

//==================================================== 菜单状态标志 ====================================================

extern uint8 menu_in_image_mode;                                                // 图像显示模式标志（1=IPS200正在显示摄像头图像）
extern uint8 menu_need_refresh;                                                 // 菜单刷新标志（1=需要重绘屏幕）

//==================================================== 菜单函数声明 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     菜单初始化（创建菜单树 + 环形链表初始化）
// 参数说明     void
// 返回参数     void
// 使用示例     menu_init();
//-------------------------------------------------------------------------------------------------------------------
void menu_init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     在 IPS200 上绘制当前菜单界面
// 参数说明     void
// 返回参数     void
// 使用示例     menu_show_All();
// 备注信息     绘制内容：
//               第0行：标题栏（当前路径，蓝底白字）
//               第1~13行：菜单项列表（箭头标记当前选中项）
//               第14行：操作提示栏
//-------------------------------------------------------------------------------------------------------------------
void menu_show_All(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     菜单"上"操作（KEY_1触发）
// 参数说明     is_long_press —— 0=短按, 1=长按（编辑模式下长按=10倍快速调节）
// 返回参数     void
// 使用示例     Menu_upFuntion(0);  // 短按上
//-------------------------------------------------------------------------------------------------------------------
void Menu_upFuntion(uint8 is_long_press);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     菜单"下"操作（KEY_2触发）
// 参数说明     is_long_press —— 0=短按, 1=长按
// 返回参数     void
//-------------------------------------------------------------------------------------------------------------------
void Menu_downFuntion(uint8 is_long_press);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     菜单"确认"操作（KEY_3触发）
// 参数说明     void
// 返回参数     void
// 备注信息     文件夹→进入子菜单，布尔→切换，数值→选中/保存编辑
//              image 文件夹 → 进入图像显示模式
//-------------------------------------------------------------------------------------------------------------------
void Menu_enterFuntion(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     菜单"返回/切换"操作（KEY_4触发）
// 参数说明     void
// 返回参数     void
// 备注信息     普通模式→返回上级，编辑模式→切换步进值/取消编辑
//              图像模式→退出图像显示
//-------------------------------------------------------------------------------------------------------------------
void Menu_quitFuntion(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     图像显示模式处理（在主循环中调用）
// 参数说明     void
// 返回参数     void
// 使用示例     当 menu_in_image_mode==1 时在主循环中调用
// 备注信息     在 IPS200 上显示摄像头灰度图像，检测 KEY_4 退出
//-------------------------------------------------------------------------------------------------------------------
void menu_image_display_process(void);

#endif
