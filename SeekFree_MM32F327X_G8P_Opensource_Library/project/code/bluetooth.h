/*********************************************************************************************************************
* 文件名称          bluetooth
* 功能描述          智能车摄像头扫描巡线 - HC-04 蓝牙透传模块头文件
* 适用平台          MM32F327X_G8P
* 说明              HC-04 是 UART 透传蓝牙模块，配对后相当于无线串口
*                   发送到 MCU TX 引脚的数据会无线传输到手机端
*                   手机端发送的数据会从 MCU RX 引脚接收
*
* 接线：
*   HC-04 TX  → MCU RX (UART3_RX_C11)
*   HC-04 RX  → MCU TX (UART3_TX_C10)
*   HC-04 VCC → 5V
*   HC-04 GND → GND
*
* 默认波特率：9600（HC-04 出厂默认，可通过 AT 命令修改）
*********************************************************************************************************************/

#ifndef __BLUETOOTH_H_
#define __BLUETOOTH_H_

#include "zf_common_headfile.h"

//==================================================== 蓝牙引脚与参数配置 ====================================================

#define BLUETOOTH_UART              (UART_6)                                    // HC-04 使用的串口号
#define BLUETOOTH_BAUD              (9600)                                      // HC-04 默认波特率 9600
#define BLUETOOTH_TX_PIN            (UART6_TX_C6)                               // MCU-TX(C6) → HC-04-RX
#define BLUETOOTH_RX_PIN            (UART6_RX_C7)                               // MCU-RX(C7) → HC-04-TX

//==================================================== 函数声明 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     蓝牙模块初始化（配置 UART）
// 参数说明     void
// 返回参数     void
// 使用示例     bluetooth_init();
// 备注信息     必须在 main() 初始化阶段调用
//-------------------------------------------------------------------------------------------------------------------
void bluetooth_init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     通过蓝牙向手机发送格式化字符串（类似 printf）
// 参数说明     fmt —— 格式化字符串（printf 格式）
// 参数说明     ... —— 可变参数
// 返回参数     void
// 使用示例     serial_printf("speed=%d angle=%.1f\r\n", speed, angle);
// 备注信息     内部使用 vsprintf + uart_write_string
//              注意：不要在中断中调用（SPI 写 UART 可能耗时较长）
//-------------------------------------------------------------------------------------------------------------------
void serial_printf(const char *fmt, ...);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     蓝牙接收处理（在主循环中每帧调用，解析调参命令）
// 参数说明     void
// 返回参数     void
// 使用示例     bluetooth_receive_process();  // 在主循环中调用
// 备注信息     解析格式：[slider,servo_kp,0.5] 或 [slider,servo_ki,xxx] 等
//-------------------------------------------------------------------------------------------------------------------
void bluetooth_receive_process(void);

#endif
