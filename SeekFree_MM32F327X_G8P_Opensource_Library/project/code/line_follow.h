/*********************************************************************************************************************
* 文件名称          line_follow
* 功能描述          智能车摄像头扫描巡线 - 巡线主控模块头文件
* 适用平台          MM32F327X_G8P
* 说明              巡线模块是图像处理算法的上层封装
*                   负责协调摄像头图像采集与图像处理的时序
*                   并将处理结果（中线数据）提供给后续的PID控制模块使用
*
* 工作流程：
*   摄像头采集一帧图像 → 图像处理管线 → 得到中线数组 →
*   中线数据可供舵机PID和电机PID使用（后续步骤不在此代码范围内）
*
* 注意：此文件只包含巡线相关的内容
*       舵机PID控制和电机PID控制的具体实现在后续步骤中完成
*       本代码到"中线数组存储完毕"为止
*********************************************************************************************************************/

#ifndef _line_follow_h_
#define _line_follow_h_

#include "zf_common_headfile.h"
#include "image_process.h"
#include "common_Mymenu.h"

//==================================================== 巡线状态定义 ====================================================

// 巡线工作状态枚举
typedef enum
{
    LINE_STATE_IDLE,                                                            // 空闲状态（未开始巡线）
    LINE_STATE_WAIT_IMAGE,                                                      // 等待图像采集完成
    LINE_STATE_PROCESSING,                                                      // 正在处理图像
    LINE_STATE_DONE,                                                            // 图像处理完成，中线数据可用
}line_follow_state_enum;

//==================================================== 外部变量声明 ====================================================







extern line_follow_state_enum line_state;                                       // 巡线当前工作状态
extern uint8 line_data_ready;                                                   // 中线数据就绪标志（1=可被PID控制模块读取）

//==================================================== 函数声明 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     巡线模块初始化
// 参数说明     void
// 返回参数     void
// 使用示例     line_follow_init();
// 备注信息     在 main() 初始化阶段调用，设置巡线模块的初始状态
//-------------------------------------------------------------------------------------------------------------------
void line_follow_init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     巡线主处理函数（每帧调用一次）
// 参数说明     void
// 返回参数     void
// 使用示例     在主循环 while(1) 中调用 line_follow_process();
// 备注信息     内部会检测 mt9v03x_finish_flag 标志
//              当一帧图像采集完成后自动触发图像处理管线
//              处理完成后将 line_data_ready 置1
//-------------------------------------------------------------------------------------------------------------------
void line_follow_process(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取指定行的赛道中线X坐标
// 参数说明     row —— 行号（0 ~ IMG_H-1）
// 返回参数     uint8 —— 该行的中线X坐标（0 ~ IMG_W-1）
// 使用示例     uint8 mid_x = get_center_line(89);  // 获取最底行的中线
// 备注信息     row 参数会被限幅到有效范围
//              如果中线数据尚未就绪（line_data_ready==0），返回图像中心值 IMG_W/2
//-------------------------------------------------------------------------------------------------------------------
uint8 get_center_line(uint8 row);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取指定行的左边界X坐标
// 参数说明     row —— 行号（0 ~ IMG_H-1）
// 返回参数     uint8 —— 该行的左边界X坐标
// 使用示例     uint8 left_x = get_left_boundary(89);
// 备注信息     如果边界数据尚未就绪，返回0
//-------------------------------------------------------------------------------------------------------------------
uint8 get_left_boundary(uint8 row);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取指定行的右边界X坐标
// 参数说明     row —— 行号（0 ~ IMG_H-1）
// 返回参数     uint8 —— 该行的右边界X坐标
// 使用示例     uint8 right_x = get_right_boundary(89);
// 备注信息     如果边界数据尚未就绪，返回 IMG_W-1
//-------------------------------------------------------------------------------------------------------------------
uint8 get_right_boundary(uint8 row);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取当前二值化阈值
// 参数说明     void
// 返回参数     uint8 —— 当前使用的二值化阈值
// 使用示例     uint8 thresh = get_current_threshold();
// 备注信息     可用于调试或显示当前的阈值
//-------------------------------------------------------------------------------------------------------------------
uint8 get_current_threshold(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     计算车身偏差（横向偏移量）
// 参数说明     look_ahead_rows —— 前瞻行数（从底部向上偏移的行数，0表示看最底行）
// 返回参数     int16 —— 偏差值（正值=中线偏右/车偏左，负值=中线偏左/车偏右）
// 使用示例     int16 error = calc_deviation(15);  // 前瞻15行
// 备注信息     偏差 = 前瞻行的中线X坐标 - 图像中心X坐标(IMG_W/2=70)
//              正值：中线在图像中心右侧 → 车身偏向赛道左侧 → 需要向右修正
//              负值：中线在图像中心左侧 → 车身偏向赛道右侧 → 需要向左修正
//-------------------------------------------------------------------------------------------------------------------
int16 calc_deviation(uint8 look_ahead_rows);


//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取中线的加权位置（用于PID控制）
// 参数说明     center_line —— 中线数组指针（长度为 IMG_H）
// 返回参数     float —— 加权位置（0~IMG_W-1）
// 使用示例     float weight_pos = get_weight_position(center_line);
// 备注信息     加权位置 = (所有中线X坐标的加权平均)
//-------------------------------------------------------------------------------------------------------------------
float get_weight_position(uint8 *center_line);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     pid计算舵机输出值
// 参数说明     target —— 目标值（期望位置）
// 参数说明     actual —— 实际值（当前偏差）
// 返回参数     float —— 舵机输出值（用于控制舵机转角）
// 使用示例     float servo_output = servo_pid_set(target, actual);
// 备注信息     使用PID算法计算舵机的控制输出
//              其中 target 是期望位置，actual 是当前偏差
//              输出值可用于调整舵机角度以纠正偏差
//-------------------------------------------------------------------------------------------------------------------
float servo_pid_set(float target,float actual);


#endif
