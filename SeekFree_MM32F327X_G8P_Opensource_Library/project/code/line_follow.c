//-------------------------------------------------------------------------------------------------------------------
// 函数名称：calc_deviation
// 功能：计算车身相对于赛道中心的横向偏差
// 参数：look_ahead_rows —— 前瞻行数（从底部向上偏移的行数）
//                         0 表示看最底行（IMG_H-1）
//                         15 表示看第 IMG_H-1-15 行
// 返回：int16 —— 偏差值
//         正值：中线在图像中心右侧 → 车身偏左 → 需要向右修正
//         负值：中线在图像中心左侧 → 车身偏右 → 需要向左修正
//         0：车身在赛道正中心
// 说明：
//   图像中心X坐标 = IMG_W/2 = 70
//   偏差 = 前瞻行的中线X - 70
//   例如：
//     - 中线在75列: 偏差 = +5  → 车身偏左，应向右修正
//     - 中线在65列: 偏差 = -5  → 车身偏右，应向左修正
//     - 中线在70列: 偏差 = 0   → 车身居中，不需修正
//
//   前瞻距离越大（look_ahead_rows越大），看的越远，
//   适合高速时使用，可以提前预判弯道趋势
//   前瞻距离越小，看的越近，适合低速时使用，响应更及时
//-------------------------------------------------------------------------------------------------------------------
int16 calc_deviation(uint8 look_ahead_rows)
{
    uint8 row;
    uint8 mid_x;

    // ---- 计算目标行号（从底部向上偏移） ----
    if(look_ahead_rows >= IMG_H)
        look_ahead_rows = IMG_H - 1;

    row = IMG_H - 1 - look_ahead_rows;                                          // 目标行号

    // ---- 获取该行的中线X坐标 ----
    mid_x = get_center_line(row);

    // ---- 计算偏差（相对于图像中心列的偏移量） ----
    // IMG_W/2 = 40 是图像的水平中心位置
    return (int16)mid_x - (int16)(IMG_W / 2);
}

float get_weight_position(uint8 *center_line)
{

    float weighted_sum = 0.0f;
    float weight_total = 0.0f;

    int16 i;
    for(i = 0; i < IMG_H; i++)
    {
        if(center_line_valid[i] == 1)                                           // 只使用真实边界点对应的中线
        {
            weighted_sum += (float)center_line[i] * weight[i];
            weight_total += weight[i];
        }
    }

    if(weight_total > 0.0f)
        return weighted_sum / weight_total;
    else
        return 0.0f;
}



float servo_pid_error=0;
float servo_pid_outd=0;
float servo_pid_outp=0;
float servo_pid_outi=0;
float servo_pid_set(float target,float actual)
{
    servo_pid_error = target - actual;
    servo_pid_outd = (servo_pid_error - servo_pid_outp)*servo_lowpass+servo_pid_outd*(1-servo_lowpass);
    servo_pid_outp = servo_pid_error;
    servo_pid_outi += servo_pid_error;
    return (servo_kp*servo_pid_outp + servo_kd*servo_pid_outd + servo_ki*servo_pid_outi);
}


