/*********************************************************************************************************************
* 文件名称          image_process
* 功能描述          智能车摄像头扫描巡线 - 图像处理算法头文件
* 适用平台          MM32F327X_G8P
* 摄像头型号        MT9V03X 总钻风灰度摄像头
* 图像尺寸          60行 × 80列（MT9V03X_H × MT9V03X_W）
* 说明              包含以下图像处理算法的声明：
*                     1. 快速大津法（OTSU）自适应二值化阈值计算
*                     2. 图像二值化（根据阈值将灰度图转为黑白二值图）
*                     3. 图像外围画黑框（为八邻域爬线提供安全边界）
*                     4. 八邻域边界追踪（爬线算法）提取赛道左右边界
*                     5. 寻找A/B/C/D四个关键点（用于判断十字路口和补线）
*                     6. 赛道中线提取（逐行计算左右边界的中点）
*                     7. 十字路口补线处理
*********************************************************************************************************************/

#ifndef _image_process_h_
#define _image_process_h_

#include "zf_common_headfile.h"

//==================================================== 图像尺寸定义 ====================================================

// 图像尺寸与 MT9V03X 摄像头配置一致
// 注意：这些值必须与 zf_device_mt9v03x.h 中的 MT9V03X_W 和 MT9V03X_H 保持一致
#define IMG_W                   MT9V03X_W                                       // 图像宽度 = 80 列
#define IMG_H                   MT9V03X_H                                       // 图像高度 = 60 行
#define IMG_SIZE                (IMG_W * IMG_H)                                 // 图像总像素数 = 4800

//==================================================== 二值化常量定义 ====================================================

#define BLACK                   0                                               // 二值化黑色值（代表赛道边界线 / 赛道外）
#define WHITE                   255                                             // 二值化白色值（代表赛道可行驶区域）

//==================================================== 八邻域搜索常量 ====================================================

#define BOUNDARY_SEARCH_MAX     140                                             // 八邻域爬线最多搜索的点数
#define BOUNDARY_SEARCH_END     5                                               // 八邻域搜索的下边界截止行号
                                                                                // 低于此行号认为已经超出搜索范围，停止爬线

//==================================================== 边界点数据结构 ====================================================

// 边界点结构体 —— 用于存储八邻域爬线算法找到的赛道边界点
// 左边界点结构体
typedef struct
{
    int16 row;                                                                  // 行坐标（Y轴，图像从上到下 0 ~ IMG_H-1）
    int16 col;                                                                  // 列坐标（X轴，图像从左到右 0 ~ IMG_W-1）
    uint8 flag;                                                                 // 存在标志（1=该点是有效的边界点，0=无效/未使用）
}edge_point;

//==================================================== 全局变量声明 ====================================================

// ---- 二值化阈值 ----
extern uint8 otsu_threshold;                                                    // 大津法计算得到的二值化阈值（0~255）
extern uint8 last_threshold;                                                    // 上一次有效的二值化阈值（用于异常时回退）
extern float filtered_threshold;                                                // 互补滤波后的平滑阈值（用于实际二值化）
extern uint8 threshold_mode;                                                    // 阈值模式：1=大津法，0=固定阈值
extern uint8 fixed_threshold;                                                   // 固定阈值（仅在 threshold_mode=0 时使用）

// ---- 二值化后的图像 ----
extern uint8 binary_image[IMG_H][IMG_W];                                        // 二值化后的图像数组
                                                                                // 0(BLACK) = 边界线/暗区域
                                                                                // 255(WHITE) = 赛道/亮区域

// ---- 八邻域爬线相关全局变量 ----
extern edge_point left_edge[BOUNDARY_SEARCH_MAX];                               // 左边界点数组（最多存储140个点）
extern edge_point right_edge[BOUNDARY_SEARCH_MAX];                              // 右边界点数组（最多存储140个点）
extern uint8 left_edge_count;                                                   // 实际找到的左边界点个数
extern uint8 right_edge_count;                                                  // 实际找到的右边界点个数
extern uint8 left_find_flag;                                                    // 左边界起始点找到标志（1=找到）
extern uint8 right_find_flag;                                                   // 右边界起始点找到标志（1=找到）
extern int16 left_start_row, left_start_col;                                    // 左边界搜索起始点坐标
extern int16 right_start_row, right_start_col;                                  // 右边界搜索起始点坐标
extern uint8 left_lose_rows;                                                    // 左边界丢失的行数
extern uint8 right_lose_rows;                                                   // 右边界丢失的行数

// ---- A/B/C/D 关键点 ----
extern uint8 point_A_row, point_A_col;                                          // A点 —— 左边界底部起点（靠近车身）
extern uint8 point_B_row, point_B_col;                                          // B点 —— 右边界底部起点（靠近车身）
extern uint8 point_C_row, point_C_col;                                          // C点 —— 左边界上部拐点（向上追踪的转折点）
extern uint8 point_D_row, point_D_col;                                          // D点 —— 右边界上部拐点（向上追踪的转折点）

// ---- 赛道中线 ----
extern uint8 center_line[IMG_H];                                                // 中线数组，center_line[i] 表示第 i 行的中线X坐标
extern uint8 left_boundary[IMG_H];                                              // 左边界数组，left_boundary[i] 表示第 i 行的左边界X坐标
extern uint8 right_boundary[IMG_H];                                             // 右边界数组，right_boundary[i] 表示第 i 行的右边界X坐标

//==================================================== 函数声明 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     快速大津法（OTSU）计算二值化阈值
// 参数说明     image —— 原始灰度图像数组指针（一维数组，长度为 IMG_SIZE）
// 返回参数     uint8 —— 最佳二值化阈值（0~255）
// 使用示例     uint8 thresh = otsu_threshold_calc((uint8 *)mt9v03x_image);
// 备注信息     优化策略：每隔2行2列采样（只用1/4像素），大幅降低计算量
//              同时检测阈值的合理性（90~130之间），异常时沿用上次有效阈值
//              原理：最大化类间方差，使前景（赛道）和背景（边界线）分离度最大
//-------------------------------------------------------------------------------------------------------------------
uint8 otsu_threshold_calc(uint8 *image);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     图像二值化
// 参数说明     src_image  —— 原始灰度图像数组指针（一维数组，长度为 IMG_SIZE）
// 参数说明     dst_image  —— 输出二值化图像数组指针（二维数组，IMG_H × IMG_W）
// 参数说明     threshold  —— 二值化阈值
// 返回参数     void
// 使用示例     image_binarize((uint8 *)mt9v03x_image, binary_image, otsu_threshold);
// 备注信息     灰度值 > 阈值 → WHITE(255) 赛道区域
//              灰度值 ≤ 阈值 → BLACK(0)   边界线 / 暗区域
//-------------------------------------------------------------------------------------------------------------------
void image_binarize(uint8 *src_image, uint8 dst_image[IMG_H][IMG_W], uint8 threshold);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     在图像外围画黑色边框
// 参数说明     image —— 二值化图像数组（IMG_H × IMG_W）
// 返回参数     void
// 使用示例     image_draw_border(binary_image);
// 备注信息     将图像最外2行和最外2列设为黑色(BLACK=0)
//              目的：防止八邻域爬线时访问越界，提供"安全护栏"
//              上方2行 + 左右2列 + 下方2行全部涂黑
//-------------------------------------------------------------------------------------------------------------------
void image_draw_border(uint8 image[IMG_H][IMG_W]);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     在图像底部寻找左右边界的起始点
// 参数说明     image —— 二值化图像数组（IMG_H × IMG_W）
// 返回参数     void
// 使用示例     find_boundary_start(binary_image);
// 备注信息     从图像最底行（IMG_H-1）向上扫描
//              从中间列（IMG_W/2=40）向左找左边界起始点（黑白交界处）
//              从中间列向右找右边界起始点（黑白交界处）
//              找到后设置 left_find_flag / right_find_flag 和起始点坐标
//-------------------------------------------------------------------------------------------------------------------
void find_boundary_start(uint8 image[IMG_H][IMG_W]);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     八邻域边界追踪（爬线算法）
// 参数说明     image —— 二值化图像数组（IMG_H × IMG_W）
// 返回参数     void
// 使用示例     boundary_trace(binary_image);
// 备注信息     从左右边界起始点出发，在8个相邻方向中寻找"黑白交界"的像素
//              将找到的像素作为下一个边界点，迭代追踪直到找不到下一个点或越界
//              八邻域方向编号：
//                左上(7)  正上(0)  右上(6)
//                正左(4)   当前点   正右(5)
//                左下(3)  正下(1)  右下(2)
//              会记录"来向"避免走回头路导致死循环
//              结果存储在 left_edge[] 和 right_edge[] 数组中
//-------------------------------------------------------------------------------------------------------------------
void boundary_trace(uint8 image[IMG_H][IMG_W]);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     寻找A/B/C/D四个关键点
// 参数说明     image —— 二值化图像数组（IMG_H × IMG_W）
// 返回参数     void
// 使用示例     find_key_points(binary_image);
// 备注信息     A/B点直接复用 find_boundary_start 的结果（避免重复扫描）
//             C点 —— 左边界线向上追踪，上方出现白点（边界断开）处为拐点
//             D点 —— 右边界线向上追踪，上方出现白点（边界断开）处为拐点
//-------------------------------------------------------------------------------------------------------------------
void find_key_points(uint8 image[IMG_H][IMG_W]);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     十字路口判断与补线处理
// 参数说明     image —— 二值化图像数组（IMG_H × IMG_W）
// 返回参数     void
// 使用示例     crossroad_fix(binary_image);
// 备注信息     判断条件：
//               1. |CY - DY| < 10 —— C和D的高度差小于10行（左右拐点高度接近）
//               2. CY > 20 且 DY > 20 —— 拐点在前方较远处（行号大=距离远）
//             补线方法：
//               在十字路口处沿AC方向和BD方向画延长线（黑色）
//               使赛道边界线在十字路口处不断开
//-------------------------------------------------------------------------------------------------------------------
void crossroad_fix(uint8 image[IMG_H][IMG_W]);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     提取赛道中线（使用八邻域爬线得到的边界点）
// 参数说明     image —— 二值化图像数组（IMG_H × IMG_W）（保留兼容，内部不再扫描图像）
// 返回参数     void
// 使用示例     extract_centerline(binary_image);
// 备注信息     直接使用 left_edge[] 和 right_edge[] 八邻域边界点计算中线：
//               1. 将边界点按行映射到 left_boundary[] / right_boundary[]
//               2. 空缺行通过上下最近有效值插值填充
//               3. 逐行计算 center_line[i] = (left_boundary[i] + right_boundary[i]) / 2
//             结果存储在：
//               center_line[]    —— 每行的中线X坐标
//               left_boundary[]  —— 每行的左边界X坐标
//               right_boundary[] —— 每行的右边界X坐标
//-------------------------------------------------------------------------------------------------------------------
void extract_centerline(uint8 image[IMG_H][IMG_W]);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     完整的图像处理流程（一站式调用）
// 参数说明     void
// 返回参数     void
// 使用示例     image_process_pipeline();
// 备注信息     依次执行以下处理步骤：
//               1. 大津法计算二值化阈值
//               2. 图像二值化
//               3. 图像外围画黑框
//               4. 寻找边界起始点
//               5. 八邻域边界追踪
//               6. 寻找A/B/C/D关键点
//               7. 十字路口补线
//               8. 提取赛道中线
//             所有结果存储在全���变量中供后续使用
//-------------------------------------------------------------------------------------------------------------------
void image_process_pipeline(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     清除边界点数据
// 参数说明     void
// 返回参数     void
// 使用示例     clear_edge_data();
// 备注信息     在每次开始新一轮边界搜索前调用
//              清除 left_edge[]、right_edge[] 数组中的所有数据
//              重置 left_edge_count、right_edge_count 等计数器
//-------------------------------------------------------------------------------------------------------------------
void clear_edge_data(void);

#endif
